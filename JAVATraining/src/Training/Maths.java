package Training;

public class Maths {
	public void add(int a, int b) {
		int no1 = a;
		int no2 = b;
		int addition = no1+no2;
		System.out.println("\nAddition is = "+addition+"\n");
	}

	public void multiply(int a, int b) {
		int no1 = a;
		int no2 = b;
		int multiply = no1*no2;
		System.out.println("Multiply = "+multiply+"\n");
	}

	public void sub(int a, int b) {
		int no1 = a;
		int no2 = b;
		int substraction = no1-no2;
		System.out.println("Substraction = "+substraction+"\n");
	}

	public void div(int a, int b) {
		int no1 = a;
		int no2 = b;
		int division = no1/no2;
		System.out.println("Division = "+division+"\n");
	}

	public void square(int a, int b) {
		int no1 = a;
		int no2 = b;
		int square1 = no1*no1;
		int square2 = no2*no2;
		System.out.println("Square of No 1 = "+square1+"\nSquare of No 2 = "+square2+"\n");
	}

	public void cube(int a, int b) {
		int no1 = a;
		int no2 = b;
		int cube1 = no1*no1*no1;
		int cube2 = no2*no2*no2;
		System.out.println("Cube of No 1 = "+cube1+"\nCube of No 2 = "+cube2+"\n");
	}
}